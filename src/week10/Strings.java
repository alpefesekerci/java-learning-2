package week10;

public class Strings {
    static void main(String[] args) {
        String sehir1 = "Istanbul";
        String sehir2 = "Ankara";
        String sehir3 = "Izmir";
        String sehir4 = "Zonguldak";

        System.out.println("Sehirlerin harf sayilari");
        System.out.println("------------------------");
        System.out.println("Istanbul: " + sehir1.length());
        System.out.println("Ankara: " + sehir2.length());
        System.out.println("Izmir: " + sehir3.length());
        System.out.println("Zonguldak: " + sehir4.length());
    }
}
