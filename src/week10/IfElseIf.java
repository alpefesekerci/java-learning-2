package week10;

public class IfElseIf {
    static void main(String[] args) {
        int not = 67;

        if (not >= 90){
            System.out.println("A");
        } else if (not >= 75){
            System.out.println("B");
        } else if (not >= 60) {
            System.out.println("C");
        } else if (not >= 50){
            System.out.println("D");
        } else {
            System.out.println("F");
        }
    }
}
